async def run_evaluation(
    dataset_path: Path, 
    config: Config
):
    """Runs the full pipeline: Convert -> Diarize -> Embed -> Transcribe -> Score."""
    print("=" * 60)
    print(f"RUNNING EVALUATION ON: {dataset_path.name}")
    print("=" * 60)

    # 1. Find all target audio files (WAV, MP3, etc.) recursively
    audio_extensions = ["*.wav", "*.mp3", "*.m4a", "*.flac"]
    all_audio_files = []
    for ext in audio_extensions:
        all_audio_files.extend(glob.glob(str(dataset_path.joinpath("**", ext)), recursive=True))
    if not all_audio_files:
        print("No audio files found in this dataset split.")
        return

    print(f"Found {len(all_audio_files)} audio files to test.")

    transcribe_results = []
    
    # Process files sequentially for detailed logging
    for audio_path_str in all_audio_files:
        audio_path = Path(audio_path_str)
        print(f"\n--- Processing {audio_path.name} ---")
        
        # A. Load Ground Truth (optional - for scoring only)
        reference_data = await load_reference_data(audio_path)
        
        # B. Run the Pipeline (using the existing function)
        transcribe_path = str(audio_path.with_suffix(".wav")) # Use a fixed temporary path for testing
        
        # To run transcribe_file, we need a full source path. We'll use the audio_path as a proxy.
        output_path = await transcribe_file(
            str(audio_path), 
            config, 
            TranscriptionClient(config), 
            progress_bar=None
        )

        if output_path:
            print(f"Transcription completed: {output_path}")
            
            # C. Compare Results (only if reference data exists)
            if reference_data:
                # Since transcribe_file is run, it should have output segments internally.
                # We need to simulate fetching those segments for comparison.
                # This is a conceptual step. We assume the writer used the data.
                
                # For now, grab the conceptual segments from the mock written file.
                mock_generated_segments = await fetch_mock_segments(output_path)
                
                score, metrics = calculate_score(mock_generated_segments, reference_data["segments"])
                
                print(f"Evaluation Complete for {audio_path.name}:")
                print(f"  OVERLAP SCORE: {score:.3f}")
                print(f"  WRT: {metrics['WER']*100:.1f}%")
                print(f"  DIARIZATION ERROR: {metrics['Diarization Error']*100:.1f}%")
                
                transcribe_results.append({"file": audio_path.name, "score": score, "metrics": metrics})
            else:
                print("No reference data available - transcription test only (no scoring).")
        else:
            print("Transcription failed.")

# Helper to simulate data extraction
async def fetch_mock_segments(output_path: str) -> list[dict]:
    """Reads the JSON/TXT output to extract segments for scoring."""
    # Re-reading the output file is the simplest robust way for a mock test.
    # Assuming JSON for structured reading.
    # (Real implementation might need to parse the file directly before cleanup)
    try:
        import json
        with open(output_path, 'r') as f:
            data = json.load(f)
            return data.get("segments", [])
    except Exception:
        log.warn("Could not read output file mock data for scoring.", file=output_path)
        return []

async def main_evaluate(config: Config):
    """Entry point for running evaluations across all datasets."""
    
    # 1. Target Datasets
    datasets = [
        Path("tests/mini-test-data/dataset/train"), 
        Path("tests/mini-test-data/dataset/valid"), 
        Path("tests/mini-test-data/dataset/test")
    ]
    
    # 2. Run full evaluation loop
    for dset_path in datasets:
        await run_evaluation(dset_path, config)

if __name__ == "__main__":
    # Requires a configuration load
    config = Config.from_env()
    asyncio.run(main_evaluate(config))
